package Console;

public class Random {

    public int random_1_20()
    {
        return (int) (Math.random() * 20);
    }
    public int random_1_10()
    {
        return (int) (Math.random() * 10);
    }
    public int random_1_5()
    {
        return (int) (Math.random() * 5);
    }
    public int random_1_2()
    {
        return (int) (Math.random() * 2);
    }
}
